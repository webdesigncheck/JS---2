function myFunction() {
   var broj = prompt("Insert number between 1 and 999");
   var x = (broj)
   if (x > 0 && x <= 999) {
      var sqrt =  Math.sqrt(x);
   alert(sqrt)
   }
   if (isNaN(x)) {
      alert("This is not a number!")
   }
   if (x>999) {
      alert("Number must be between 1 i 999!");
   }
}

